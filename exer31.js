let diaSemana = 9;
switch (diaSemana) {
    case 1:
        console.log('domingo');
        break;
    case 2:
        console.log('segunda');
        break;
        
    case 3:
        console.log('terça');
        break;
        
    case 4:
        console.log('quarta');
        break;
        
    case 6:
        console.log('quinta');
        break;
    case 7:
        console.log('sexta');
        break;
    case 8:
        console.log('sábado');
        break;
    default:
        console.log('Indisponível')
}